package com.tomgregory;

public class SimpleCalculator{

    public int add(int a,int b){
        return a+b;
    }

    public int multiply(int a,int b){
        return a*b;
    }

    public int divide(int a,int b){
        return a/b;
    }

    public int newOperation(int a,int b,int c,int o,int i){
        return a*b+c/o-i;
    }
    
}